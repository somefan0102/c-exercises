int n_isupper_1(int ch) {
     return ((ch == 'A') || (ch == 'B') || (ch == 'C') || (ch == 'D') || (ch == 'E') || (ch == 'F') || (ch == 'G') ||
     (ch == 'H') || (ch == 'I') || (ch == 'J') || (ch == 'K') || (ch == 'L') || (ch == 'M') || (ch == 'N') || (ch == 'O') ||
     (ch == 'P') || (ch == 'Q') || (ch == 'R') || (ch == 'S') || (ch == 'T') || (ch == 'U') || (ch == 'V') || (ch == 'W') ||
     (ch == 'X') || (ch == 'Y') || (ch == 'Z'));
}

int n_isupper_2(int ch) {
     assert(ch >= 0 && ch < 128);
     return chars[ch].is_upper;
}

int n_isupper_3(int ch) {
     return (ch >= 'A' && ch <= 'Z');
}

